// Deterministic hash so any code without coordinates still gets a stable
// (not random-per-render) position scattered within its country.
function hashCode(s: string): number {
  let h = 0;
  for (let i = 0; i < s.length; i++) h = (h * 31 + s.charCodeAt(i)) >>> 0;
  return h;
}

export function getCoords(code: string, country: string = ""): [number, number] {
  if (CITY_COORDS[code]) return CITY_COORDS[code];
  const base = COUNTRY_CENTROIDS[country] ?? [20, 0];
  const h = hashCode(code);
  const jitterLat = ((h % 600) / 100 - 3); // ±3°
  const jitterLng = (((h >> 8) % 600) / 100 - 3);
  return [base[0] + jitterLat, base[1] + jitterLng];
}
